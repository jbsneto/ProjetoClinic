    public void cadastrarCliente() {
        try {

            if (this.jtNome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o cliente");
            } else {
                Endereco e = new Endereco();
                e.setRua(this.jtRua.getText());
                e.setBairro(this.jtBairro.getText());
                if(cidadeEstado!=null)
                    e.setCidadeEstado(cidadeEstado);
                e.setNumero(this.jtNumero.getText());
                e.setCep(this.jtCep.getText());

                Cliente c = new Cliente();
                c.setEndereco(e);
                c.setNome(this.jtNome.getText());
                c.setCpf(this.jtCpf.getText());
                c.setDataNas(Util.stringToDate(this.jtDataNas.getText()));
                c.setTelefone(this.jtTelefone.getText());
                c.setEmail(this.jtEmail.getText());
                c.setObs(this.jtObs.getText());

                facade.clienteCadastrar(c);
                buscarCliente(c);
                mostraButtons();
                JOptionPane.showMessageDialog(null, "Cliente: " + c.getId() + " cadastrado com sucesso!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }
    }

    public void buscarCliente(Cliente c) {
        try {
            //if(facade.ClienteBuscar(setId)!= null){ 
            //TRAVA O CADASTRAR PARA N CADASTRAR UM BUSCADO
            //jbCadastrar.setEnabled(false);

            if (c != null) {
                cliente = c;
                this.jtId.setText(c.getId().toString());
                this.jtDataCadastro.setText(Util.dateToString(c.getDataCadastro()));
                this.jtNome.setText(c.getNome());
                this.jtCpf.setText(c.getCpf());
                this.jtDataNas.setText(Util.dateToString(c.getDataNas()));
                this.jtRua.setText(c.getEndereco().getRua());
                this.jtBairro.setText(c.getEndereco().getBairro());
                if(cidadeEstado!=null)
                    this.jtCidade.setText(cidadeEstado.toString());
                this.jtNumero.setText(c.getEndereco().getNumero());
                this.jtCep.setText(c.getEndereco().getCep());
                this.jtTelefone.setText(c.getTelefone());
                this.jtEmail.setText(c.getEmail());
                this.jtObs.setText(c.getObs());
            }else
                limparForm();

        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }

    }

    //COLOCAR TUDO PRA CLIENTE
    public void editarCliente(Cliente c) throws Exception {

        try {
            if (facade.clienteBuscar(c.getId()) != null) {
                c.setNome(this.jtNome.getText());
                c.setCpf(this.jtCpf.getText());
                c.setDataNas(Util.stringToDate(this.jtDataNas.getText()));
                c.setTelefone(this.jtTelefone.getText());
                c.setEmail(this.jtEmail.getText());
                c.setObs(this.jtObs.getText());
                c.getEndereco().setRua(this.jtRua.getText());
                c.getEndereco().setBairro(this.jtBairro.getText());
                if(cidadeEstado!=null)
                    c.getEndereco().setCidadeEstado(cidadeEstado);
                c.getEndereco().setNumero(this.jtNumero.getText());
                c.getEndereco().setCep(this.jtCep.getText());

                facade.clienteEditar(c);
                buscarCliente(c);
                mostraButtons();
                JOptionPane.showMessageDialog(null, "Cliente: " + c.getId() + " editado com sucesso!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }

    }

    public void deletarCliente(Cliente c) {
        try {
            if (facade.clienteBuscar(c.getId()) != null) {
                facade.clienteDeletar(c);
                JOptionPane.showMessageDialog(null, "Cliente: " + c.getId() + " excluido com sucesso!");
            }
        } catch (Exception ex) {
            Logger.getLogger(JfCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

