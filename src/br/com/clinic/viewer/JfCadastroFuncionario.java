    public void editarFuncionario(Funcionario f) throws Exception {

        try {
        if(f!=null){
            jtLogin.setEditable(false);
            if (facade.funcionarioBuscar(f.getId()) != null) {
                f.setNome(this.jtNome.getText());
                f.setCpf(this.jtCpf.getText());
                f.setDataNas(Util.stringToDate(this.jtDataNas.getText()));
                f.setTelefone(this.jtTelefone.getText());
                f.setEmail(this.jtEmail.getText());
                f.setObs(this.jtObs.getText());
                f.getEndereco().setRua(this.jtRua.getText());
                f.getEndereco().setBairro(this.jtBairro.getText());
                if(cidadeEstado!=null)
                    f.getEndereco().setCidadeEstado(cidadeEstado);
                f.getEndereco().setNumero(this.jtNumero.getText());
                f.getEndereco().setCep(this.jtCep.getText());
                f.setSenha(this.jpfSenha.getText());
                f.setCargo((Cargo) jcbCargo.getSelectedItem());

                if(jcbCargo.getSelectedItem() == null || jtNome.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Informe o cliente ou Cargo");
                    limparForm();
                }else{ 
                    facade.funcionarioEditar(f);
                    buscarFuncionario(f);
                    mostraButtons();
                    JOptionPane.showMessageDialog(null, "Cliente: " + f.getId() + " editado com sucesso!");    
                }
                jtLogin.setEditable(true);
            }
        }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }

    }

    public void deletarFuncionario(Funcionario f) {
        try {
            if (facade.funcionarioBuscar(f.getId()) != null) {
                facade.funcionarioDeletar(f);
                JOptionPane.showMessageDialog(null, "Cliente: " + f.getId() + " excluido com sucesso!");
            }
        } catch (Exception ex) {
            Logger.getLogger(JfCadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

