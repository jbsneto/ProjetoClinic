    public void cadastrarFuncionario() {
        try {

            if (this.jtNome.getText().equals("") || jcbCargo.getSelectedItem()==null) {
                JOptionPane.showMessageDialog(null, "Informe o cliente ou Cargo");
            } else {
                Endereco e = new Endereco();
                e.setRua(this.jtRua.getText());
                e.setBairro(this.jtBairro.getText());
                if(cidadeEstado!=null)
                    e.setCidadeEstado(cidadeEstado);
                e.setNumero(this.jtNumero.getText());
                
                e.setCep(this.jtCep.getText());

                Funcionario f = new Funcionario();
                f.setEndereco(e);
                f.setNome(this.jtNome.getText());
                f.setCpf(this.jtCpf.getText());
                f.setDataNas(Util.stringToDate(this.jtDataNas.getText()));
                f.setTelefone(this.jtTelefone.getText());
                f.setEmail(this.jtEmail.getText());
                f.setObs(this.jtObs.getText());
                
                if(jtLogin.getText() != null){
                    boolean aux = true;
                    for(Funcionario fun: facade.funcionarioListar())
                        if(jtLogin.getText().equals(fun.getLogin()))
                            aux = false;
                    if(aux){
                       f.setLogin(jtLogin.getText());
                       f.setSenha(jpfSenha.getText());
                       f.setCargo((Cargo) jcbCargo.getSelectedItem());

                        facade.funcionarioCadastrar(f);
                        buscarFuncionario(f);
                        mostraButtons();
                        JOptionPane.showMessageDialog(null, "Cliente: " + f.getId() + " cadastrado com sucesso!");
                    }else
                       JOptionPane.showMessageDialog(null, "Login já existe"); 
                } 
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }
    }

    public void buscarFuncionario(Funcionario f) {
        try {
            //if(facade.ClienteBuscar(setId)!= null){ 
            //TRAVA O CADASTRAR PARA N CADASTRAR UM BUSCADO
            //jbCadastrar.setEnabled(false);

            if (f != null) {
                funcionario = f;
                this.jtId.setText(f.getId().toString());
                this.jtDataCadastro.setText(Util.dateToString(f.getDataCadastro()));
                this.jtNome.setText(f.getNome());
                this.jtCpf.setText(f.getCpf());
                this.jtDataNas.setText(Util.dateToString(f.getDataNas()));
                this.jtRua.setText(f.getEndereco().getRua());
                this.jtBairro.setText(f.getEndereco().getBairro());
                if(cidadeEstado!=null)   
                    this.jtCidade.setText(cidadeEstado.toString());
                this.jtNumero.setText(f.getEndereco().getNumero());
                this.jtCep.setText(f.getEndereco().getCep());
                this.jtTelefone.setText(f.getTelefone());
                this.jtEmail.setText(f.getEmail());
                this.jtObs.setText(f.getObs());
                this.jtLogin.setText(f.getLogin());
                this.jpfSenha.setText(f.getSenha());
                
                DefaultComboBoxModel dm = new DefaultComboBoxModel();
                this.jcbCargo.removeAllItems();
                try {
                    if (facade.cargoListar() != null) {
                        if(f.getCargo()!=null){
                            dm.addElement(f.getCargo());
                            for (Cargo c : facade.cargoListar()) {
                                if(!Objects.equals(f.getCargo().getId(), c.getId()))
                                    dm.addElement(c);
                            }
                            jcbCargo.setModel(dm);
                        }else{
                            preencherComboBox();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Lista vazia! Cadastre setores!");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    System.out.println("Erro: " + e.getMessage());
                }

            } else {
                limparForm();
            }

        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erro: " + e.getMessage());
        }

    }

